
"""Fonction built-in len()"""

chaine_char = 'Salut !!'
la_liste = [1, 2, 3, 4, 5]
le_dictionnaire = {'a': 1, 'b': 2, 'c': 3}

# La fonction len() renvoie la longueur d'une chaîne de caractères
print(len(chaine_char))

# elle retourne aussi la longueur d'une liste
print(len(la_liste))

# elle retourne aussi la longueur d'un dictionnaire
# la fonction prend en compte les paires clé/valeur
print(len(le_dictionnaire))
